#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*a;
	unsigned char	*b;
	size_t			i;
	int				c;

	i = 0;
	a = (unsigned char*)s1;
	b = (unsigned char*)s2;
	while (i < n)
	{
		if (a[i] != b[i])
		{
			c = a[i] - b[i];
			return (c);
		}
		i++;
	}
	return (0);
}
