#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int	f;

	f = ft_strlen(s1);
	i = 0;
	while (s2[i] != '\0')
	{
		s1[f + i] = s2[i];
		i++;
	}
	s1[f + i] = '\0';
	return (s1);
}
