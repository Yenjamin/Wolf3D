#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		f;

	f = ft_strlen(s1);
	i = 0;
	while (s2[i] != '\0' && i < n)
	{
		s1[f + i] = s2[i];
		i++;
	}
	s1[f + i] = '\0';
	return (s1);
}
