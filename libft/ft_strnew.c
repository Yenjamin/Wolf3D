#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*t;

	t = malloc(size + 1);
	{
		if (t == NULL)
			return (NULL);
		else
		{
			ft_bzero(t, size + 1);
			return (t);
		}
	}
}
