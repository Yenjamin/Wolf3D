#include "libft.h"

char	*ft_strtrim(char const *s)
{
	int	i;
	int	j;

	i = 0;
	if (!s)
		return (NULL);
	j = ft_strlen(s);
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\t')
		i++;
	j--;
	while ((s[j] == ' ' || s[j] == '\n' || s[j] == '\t') && (i < j))
		j--;
	return (ft_strsub(s, i, (j - i + 1)));
}
